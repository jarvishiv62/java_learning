public class Primitives
{
    public static void main(String[] args) {
        int rollno = 62; //integer size 4 bytes
        char letter = 'U'; //character size 2 bytes
        float marks = 98.5f; //float size 4 bytes
        double largerDecimalNumber = 98.56789; //double size 8 bytes
        long largeInteger = 9876543210L; //long size 8 bytes
        byte smallInteger = 1; //byte size 1 byte
        boolean check = true; //boolean size 1 byte

        //String is not a primitive data type
    }
}
  